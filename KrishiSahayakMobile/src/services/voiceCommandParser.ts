/**
 * Parse a voice command string into a structured action
 */
export function parseVoiceCommand(text: string): VoiceCommand {
  const lower = text.toLowerCase().trim();

  if (!lower) {
    return { action: 'query', query: text, confidence: 0 };
  }

  let target: string | null = null;

  // Try each open pattern (built once at module level)
  for (const pattern of OPEN_PATTERNS) {
    const match = lower.match(pattern);
    if (match) {
      target = match[2]?.trim() || match[1]?.trim() || lower;
      break;
    }
  }

  // If no pattern matched, use the whole text
  if (!target) target = lower;

  // Find best matching screen
  let bestMatch: { screen: string; score: number } | null = null;

  for (const [key, config] of Object.entries(NAV_TARGETS)) {
    let score = 0;
    for (const keyword of config.keywords) {
      if (target.includes(keyword.toLowerCase())) {
        score = Math.max(score, keyword.length / target.length);
      }
    }
    if (target.includes(key)) score = Math.max(score, 0.6);
    if (score > 0 && (!bestMatch || score > bestMatch.score))
      bestMatch = { screen: config.screen, score };
  }

  // Check for question / price patterns
  let isQuery = false;
  for (const pattern of QUERY_PATTERNS) {
    if (pattern.test(lower)) { isQuery = true; break; }
  }

  if (bestMatch && bestMatch.score > 0.3) {
    return {
      action: 'navigate',
      screen: bestMatch.screen,
      confidence: Math.min(bestMatch.score + 0.3, 1),
    };
  }

  return {
    action: isQuery ? 'query' : 'navigate',
    screen: isQuery ? 'AiAssistant' : undefined,
    query: text,
    confidence: 0.3,
  };
}

/**
 * Get the list of all supported commands for display/hint
 */
export function getSupportedCommands(): { command: string; screen: string }[] {
  return Object.entries(NAV_TARGETS).map(([_, config]) => ({
    command: config.keywords[0],
    screen: config.screen,
  }));
}
