interface LanguageContextType {
  language: string;
  setLanguage: (code: string) => Promise<void>;
  t: (key: string, fallback?: string) => string;
  tWithVars: (key: string, vars: Record<string, string | number>, fallback?: string) => string;
  getCurrentLanguage: () => { code: string; name: string; native: string; flag: string };
  isRTL: boolean;
}

const LanguageContext = createContext<LanguageContextType | null>(null);

export const useTranslation = () => {
  const ctx = useContext(LanguageContext);
  if (!ctx) {
    return {
      language: 'hi',
      t: (key: string, fallback?: string) => fallback || key,
      tWithVars: (key: string, vars: Record<string, string | number>, fallback?: string) => fallback || key,
      getCurrentLanguage: () => ({ code: 'hi', name: 'Hindi', native: 'हिन्दी', flag: '🇮🇳' }),
      isRTL: false,
      setLanguage: async () => {},
    };
  }
  return ctx;
};

