const translations: Record<string, TranslationsDict> = { en, hi };

// ─── Language Provider ─────────────────────────────────────────
export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState('hi');

  useEffect(() => {
    (async () => {
      const saved = await storageService.getItem<string>(KEYS.SELECTED_LANGUAGE);
      if (saved) setLanguageState(saved);
    })();
  }, []);

  const setLanguage = useCallback(async (code: string) => {
    setLanguageState(code);
    await storageService.setItem(KEYS.SELECTED_LANGUAGE, code);
  }, []);

  const getNestedValue = useCallback(
    (obj: any, path: string): string | undefined => {
      const parts = path.split('.');
      let current = obj;
      for (const part of parts) {
        if (current == null || typeof current !== 'object') return undefined;
        current = current[part];
      }
      return typeof current === 'string' ? current : undefined;
    },
    []
  );

  const t = useCallback(
    (key: string, fallback?: string): string => {
      // Try current language, fall back to English, then fallback param, then key
      const langData = translations[language] || translations.en;
      let val = getNestedValue(langData, key);
      if (val) return val;
      val = getNestedValue(translations.en, key);
      if (val) return val;
      return fallback || key;
    },
    [language, getNestedValue]
  );

  const tWithVars = useCallback(
    (key: string, vars: Record<string, string | number>, fallback?: string): string => {
      let str = t(key, fallback);
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(`{{${k}}}`, String(v));
      }
      return str;
    },
    [t]
  );

  const getCurrentLanguage = useCallback(() => {
    return LANGUAGES.find(l => l.code === language) || LANGUAGES[0];
  }, [language]);

  const isRTL = language === 'ur';

  const value: LanguageContextType = {
    language,
    setLanguage,
    t,
    tWithVars,
    getCurrentLanguage,
    isRTL,
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

export default LanguageContext;
